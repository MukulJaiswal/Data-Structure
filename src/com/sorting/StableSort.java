package com.sorting;

public class StableSort {
	public static void main(String[] args) {
		/**
		 * Some sorting algorithms are stable by nature like Insertion sort, Merge Sort,
		 * Bubble Sort, etc.
		 */

		/**
		 * And some sorting algorithms are not, like Heap Sort, Quick Sort, etc.
		 */

		/**
		 * Background: a "stable" sorting algorithm keeps the items with the same
		 * sorting key in order. Suppose we have a list of 5-letter words:
		 * 
		 * peach straw apple spork
		 * 
		 * If we sort the list by just the first letter of each word then a stable-sort
		 * would produce:
		 * 
		 * apple peach straw spork
		 * 
		 * In an unstable sort algorithm, straw or spork may be interchanged,
		 * 
		 * but in a stable one, they stay in the same relative positions (that is, since
		 * straw appears before spork in the input, it also appears before spork in the
		 * output).
		 */
	}
}
